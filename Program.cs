using System;

class Автомобиль
{
    private string название;
    private int максимальнаяСкорость;

    public Автомобиль(string название, int максимальнаяСкорость)
    {
        this.название = название;
        this.максимальнаяСкорость = максимальнаяСкорость;
    }

    public void ВывестиИнформацию()
    {
        Console.WriteLine($"Автомобиль: {название}, Максимальная скорость: {максимальнаяСкорость} км/ч");
    }

    public int ПолучитьМаксимальнуюСкорость()
    {
        return максимальнаяСкорость;
    }
}

class Program
{
    static void Main()
    {
        Автомобиль автомобиль1 = new Автомобиль("Audi", 250);
        Автомобиль автомобиль2 = new Автомобиль("BMW", 260);
        Автомобиль автомобиль3 = new Автомобиль("Mercedes", 240);
        Автомобиль автомобиль4 = new Автомобиль("Ferrari", 320);

        Console.WriteLine("Гонки начались!");
        Console.WriteLine("Участвующие автомобили:");

        автомобиль1.ВывестиИнформацию();
        автомобиль2.ВывестиИнформацию();
        автомобиль3.ВывестиИнформацию();
        автомобиль4.ВывестиИнформацию();

        Автомобиль победитель = НайтиПобедителя(автомобиль1, автомобиль2, автомобиль3, автомобиль4);
        Console.WriteLine($"Победитель: {победитель.ПолучитьМаксимальнуюСкорость()} км/ч - {победитель.ПолучитьМаксимальнуюСкорость()}");

        Console.WriteLine("Нажмите любую клавишу для закрытия программы...");
        Console.ReadKey();
    }

    static Автомобиль НайтиПобедителя(params Автомобиль[] автомобили)
    {
        Автомобиль победитель = автомобили[0];

        foreach (var авто in автомобили)
        {
            if (авто.ПолучитьМаксимальнуюСкорость() > победитель.ПолучитьМаксимальнуюСкорость())
            {
                победитель = авто;
            }
        }

        return победитель;
    }
}
